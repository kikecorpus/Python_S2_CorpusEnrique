#ejercicio 10 calcular primero de una lista de numero 

# bienvenida al usuario
print("A continuación ingresa una lista de números para calcular su promedio")

# input para obtener los tres números del usuario
num1 = float(input("Escribe el primer número: "))
num2 = float(input("Escribe el segundo número: "))
num3 = float(input("Escribe el tercer número: "))

# operacion para calcular  el promedio
resultado = (num1 + num2 + num3) / 3

# imprimo el resultado
print("El promedio de los tres números ingresados es:", resultado)

