#ejercicio 7 area de un triangulo

#bienvenida al usuario
print('Bienvenido al portal para calcular el area de un triangulo')

#input para recicbir valores del usuario
print ("escribir la base del triangulo")
basedetriangulo = int(input())

print('escribe la altura del triangulo')
alturadeltriangulo = int(input())

#calcular el area
area = (basedetriangulo * alturadeltriangulo) / 2  		

#imprimir resultado
print('la base del triangulo es ', area)
